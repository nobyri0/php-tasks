<?php
/**
 * Задание 1.2: Определение типа треугольника
 * Принимает три положительных числа и определяет вид треугольника
 */

function getTriangleType($a, $b, $c) {
    if ($a <= 0 || $b <= 0 || $c <= 0) {
        return "Ошибка: все стороны должны быть положительными";
    }
    if ($a + $b <= $c || $a + $c <= $b || $b + $c <= $a) {
        return "Треугольник не существует";
    }
    if ($a == $b && $b == $c) {
        return "Равносторонний треугольник";
    } elseif ($a == $b || $b == $c || $a == $c) {
        return "Равнобедренный треугольник";
    } else {
        return "Разносторонний треугольник";
    }
}
echo "=== Определение типа треугольника ===\n\n";
echo "Тест 1: Стороны 5, 5, 5\n";
echo "Результат: " . getTriangleType(5, 5, 5) . "\n\n";
echo "Тест 2: Стороны 5, 5, 7\n";
echo "Результат: " . getTriangleType(5, 5, 7) . "\n\n";
echo "Тест 3: Стороны 3, 4, 5\n";
echo "Результат: " . getTriangleType(3, 4, 5) . "\n\n";
echo "Тест 4: Стороны 1, 2, 10\n";
echo "Результат: " . getTriangleType(1, 2, 10) . "\n\n";

echo "Введите три стороны треугольника через пробел: ";
$input = trim(fgets(STDIN));
list($a, $b, $c) = explode(' ', $input);
echo "Результат: " . getTriangleType($a, $b, $c) . "\n";
?>